#Comparando o uso de: for x while  - PARTE 1

#Usando o for (Uso para quando sei o limite)
'''for c in range(1,10):
    print(c)
print('Fim.')'''

#Usando o while (Uso nas duas situações. Quando sei e quando não sei o limite)
c = 1
while c < 10:
    print(c)
    c += 1
print('Fim')